// ─── Main ─────────────────────────────────────────────────────────────────────

async function main() {
  const seed = process.argv[2] ? parseInt(process.argv[2]) : null;
  if (!seed) { console.error('Usage: npx ts-node run-turn.ts <seed>'); process.exit(1); }

  const gameDir = path.join('games', String(seed));

  // Find the most recent state file — prefer state-after-turn-NN.json (highest NN), fall back to state.json
  const allFiles = fs.readdirSync(gameDir).filter(f => f.endsWith('.json'));
  const turnFiles = allFiles
    .map(f => ({ f, n: Number(f.match(/state-after-turn-(\d+)\.json/)?.[1] ?? -1) }))
    .filter(x => x.n >= 0)
    .sort((a, b) => b.n - a.n);
  const stateFile = turnFiles.length > 0 ? turnFiles[0].f : 'state.json';
  const statePath = path.join(gameDir, stateFile);

  console.log(`\nLoading: ${statePath}`);
  const raw = JSON.parse(fs.readFileSync(statePath, 'utf-8'));
  const state = deserializeState(raw);

  const turnNum = state.turn;
  const turnLabel = String(turnNum).padStart(2, '0');
  const turnDir = path.join(gameDir, `turn-${turnLabel}`);
  fs.mkdirSync(turnDir, { recursive: true });

  console.log(`Running turn ${turnNum} (${state.players.length} players)...\n`);

  // Load reflections file if present (generated by reflect.ts after prior turns)
  const reflections = new Map<string, string>();
  const reflDir = path.join(gameDir, 'reflections');
  if (fs.existsSync(reflDir)) {
    const reflFiles = fs.readdirSync(reflDir)
      .filter(f => f.match(/^after-turn-\d+\.md$/))
      .sort((a, b) => {
        const na = parseInt(a.match(/(\d+)/)![1]);
        const nb = parseInt(b.match(/(\d+)/)![1]);
        return nb - na;
      });
    if (reflFiles.length > 0) {
      const reflContent = fs.readFileSync(path.join(reflDir, reflFiles[0]), 'utf-8');
      // Parse each player's reflection block: "## Name — N VP" followed by text until next "##"
      const blocks = reflContent.split(/^## /m).slice(1);
      for (const block of blocks) {
        const nameMatch = block.match(/^(.+?) —/);
        if (!nameMatch) continue;
        const name = nameMatch[1].trim();
        const player = state.players.find(p => p.name === name);
        if (!player) continue;
        const text = block.replace(/^.+\n\n/, '').trim();
        reflections.set(player.id, text);
        console.log(`  Loaded reflection for ${name}`);
      }
    }
  }

  // Build agent map from player state (model stored on each player)
  const agentMap = new Map(
    state.players.map(p => [p.id, createAgent(p.id, p.name, p.model, reflections.get(p.id))])
  );

  // Shared round log — grows as each player acts; later players see full prior context
  const roundLog: TurnLog = [];

  // ── Run all players ─────────────────────────────────────────────────────────
  for (let i = 0; i < state.players.length; i++) {
    state.currentPlayerIndex = i;
    const player = state.players[i];
    const vp = calculateVP(state, player.id);

    console.log(`\n${'─'.repeat(50)}`);
    console.log(`${player.name} — ${vp} VP`);
    console.log('─'.repeat(50));

    // Insert a turn-start marker with full player state snapshot
    // Dev cards are hidden for non-active players to prevent information leaks
    const playerSnapshot = state.players.map(p => {
      const isActive = p.id === player.id;
      return {
        name: p.name,
        model: p.model,
        color: p.color,
        vp: isActive ? calculateVP(state, p.id) : calculateVisibleVP(state, p.id),
        resources: { ...p.resources },
        devCards: isActive ? [...p.devCards] : [],
        devCardCount: p.devCards.length,
      };
    });
    roundLog.push({
      phase: 'system',
      fromName: 'Game',
      fromId: null,
      publicMessage: `=== ${player.name}'s turn (turn ${turnNum}) — ${vp} VP ===`,
      data: { playerSnapshot },
    });

    await runMainTurn(state, agentMap, roundLog);

    // Check win after each player
    const winner = checkWin(state);
    if (winner) {
      state.winner = winner;
      state.phase = 'ended';
      const wp = state.players.find(p => p.id === winner)!;
      console.log(`\n★ ${wp.name} WINS with ${calculateVP(state, winner)} VP!`);
      break;
    }
  }

  // ── Build turn summary from roundLog and push to state ──────────────────────
  state.turnSummaries.push(buildTurnSummary(turnNum, roundLog, state));
  if (state.turnSummaries.length > 10) state.turnSummaries.shift(); // keep rolling window

  // Advance turn counter for next round
  if (state.phase !== 'ended') {
    state.turn++;
  }

  // ── Collect board state for map ─────────────────────────────────────────────
  const hexes = [...state.hexes.values()].map(h => ({
    id: h.id, q: h.q, r: h.r, type: h.type, number: h.number, hasRobber: h.hasRobber,
  }));
  const buildings = [...state.vertices.values()].filter(v => v.building).map(v => {
    const p = state.players.find(pl => pl.id === v.building!.playerId)!;
    return { vertexId: v.id, playerId: p.id, playerName: p.name, playerColor: playerHex(p.model, p.color), buildingType: v.building!.type };
  });
  const roads = [...state.edges.values()].filter(e => e.road).map(e => {
    const p = state.players.find(pl => pl.id === e.road)!;
    return { edgeId: e.id, playerId: p.id, playerName: p.name, playerColor: playerHex(p.model, p.color) };
  });
  const robberHex = [...state.hexes.values()].find(h => h.hasRobber);

  // Final VP for all players
  const scores = state.players.map(p => ({ ...p, colorHex: playerHex(p.model, p.color), vp: calculateVP(state, p.id) }));

  // ── Save outputs ─────────────────────────────────────────────────────────────
  const mapData = { seed, hexes, ports: PORT_DEFS, buildings, roads, robberHexId: robberHex?.id ?? null };
  fs.writeFileSync(path.join(turnDir, 'map.html'),      buildMapHtml(mapData, turnNum, scores));
  fs.writeFileSync(path.join(turnDir, 'dialogue.html'), buildDialogueHtml(seed, turnNum, roundLog, scores));

  const nextStateFile = `state-after-turn-${turnLabel}.json`;
  fs.writeFileSync(path.join(gameDir, nextStateFile), JSON.stringify(serializeState(state), null, 2));

  console.log(`\nSaved to games/${seed}/turn-${turnLabel}/`);
  console.log(`  map.html`);
  console.log(`  dialogue.html`);
  console.log(`  ../${nextStateFile}`);
  console.log(`\nScores after turn ${turnNum}:`);
  for (const p of scores.sort((a, b) => b.vp - a.vp)) {
    console.log(`  ${p.name}: ${p.vp} VP`);
  }
  console.log();
}

main().catch(e => { console.error(e); process.exit(1); });

// ─── Turn summary builder ─────────────────────────────────────────────────────

function buildTurnSummary(turnNum: number, roundLog: TurnLog, state: GameState): TurnSummary {
  const events: string[] = [];

  // Group entries by active player (between system markers)
  let currentPlayer = '';
  const trades: string[] = [];
  const builds: string[] = [];
  const robbers: string[] = [];
  const comments: string[] = [];

  for (const e of roundLog) {
    if (e.phase === 'system') {
      // Flush previous player's events
      if (currentPlayer && (trades.length || builds.length || robbers.length)) {
        const vp = calculateVP(state, state.players.find(p => p.name === currentPlayer)?.id ?? '');
        const parts: string[] = [`${currentPlayer}(${vp}VP)`];
        if (builds.length) parts.push('built: ' + builds.join(', '));
        if (trades.length) parts.push('traded: ' + trades.slice(0, 3).join('; '));
        if (robbers.length) parts.push(robbers[0]);
        events.push('  ' + parts.join(' | '));
      }
      trades.length = 0; builds.length = 0; robbers.length = 0;
      currentPlayer = e.publicMessage.match(/^=== (.+?)'s turn/)?.[1] ?? '';
      continue;
    }

    if (e.phase === 'build' && e.publicMessage && !e.publicMessage.startsWith('has nothing') && !e.publicMessage.startsWith('I ')) {
      // Only capture the actual build action lines (e.g. "builds road at ...", "upgrades to city at ...", "buys a dev card")
      if (e.publicMessage.match(/^(builds|upgrades|buys|bank-trades)/)) {
        const short = e.publicMessage.replace(/ at [^\s]+ .+$/, ' [placed]').replace(/ at [^\s]+$/, ' [placed]');
        builds.push(short);
      }
    }

    if (e.phase === 'trade_resolve' && e.publicMessage.startsWith('✓ TRADE')) {
      // "✓ TRADE: X gives A, receives B from Y"
      const m = e.publicMessage.match(/✓ TRADE[^:]*: (.+?) gives (.+?), receives (.+?) from (.+)/);
      if (m) trades.push(`${m[1]}→${m[4]}: ${m[2]} for ${m[3]}`);
    }

    if (e.phase === 'robber' && e.publicMessage.startsWith('moves robber')) {
      const m = e.publicMessage.match(/moves robber to ([^,]+), steals .+ from (.+)/);
      if (m) robbers.push(`robber→${m[1]}, stole from ${m[2]}`);
      else robbers.push('moved robber');
    }

    if (e.phase === 'commentary' && e.fromName) {
      comments.push(`${e.fromName}: "${e.publicMessage.slice(0, 80)}${e.publicMessage.length > 80 ? '…' : ''}"`);
    }
  }

  // Flush last player
  if (currentPlayer && (trades.length || builds.length || robbers.length)) {
    const vp = calculateVP(state, state.players.find(p => p.name === currentPlayer)?.id ?? '');
    const parts: string[] = [`${currentPlayer}(${vp}VP)`];
    if (builds.length) parts.push('built: ' + builds.join(', '));
    if (trades.length) parts.push('traded: ' + trades.slice(0, 3).join('; '));
    if (robbers.length) parts.push(robbers[0]);
    events.push('  ' + parts.join(' | '));
  }

  // Notable commentary (deduplicated, max 3)
  const seen = new Set<string>();
  const uniqueComments = comments.filter(c => {
    const key = c.slice(0, 40);
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  }).slice(0, 3);
  if (uniqueComments.length) {
    events.push('  Notable: ' + uniqueComments.join(' | '));
  }

  return { turn: turnNum, events };
}

