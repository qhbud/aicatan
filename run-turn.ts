function buildDialogueHtml(
  seed: number,
  turnNum: number,
  roundLog: TurnEntry[],
  scores: Array<{ id: string; name: string; color: string; colorHex: string; vp: number }>,
): string {
  // Map player name → resolved hex color
  const playerHexMap: Record<string, string> = {};
  for (const s of scores) playerHexMap[s.name] = s.colorHex;

  let html = '';
  for (const entry of roundLog) {
    if (entry.phase === 'system') {
      const snap = (entry.data as any)?.playerSnapshot as Array<{
        name: string; model: string; color: string; vp: number;
        resources: Record<string,number>; devCards: string[]; devCardCount: number;
      }> | undefined;

      let snapHtml = '';
      if (snap) {
        const rows = snap.map(p => {
          const col = playerHexMap[p.name] ?? '#9ab8d8';
          const res = ['wood','brick','wheat','sheep','ore']
            .map(r => `<span style="opacity:0.7;font-size:0.8em">${r[0].toUpperCase()}:</span><b>${p.resources[r]??0}</b>`)
            .join(' ');
          // Show dev card types only for the active player (devCards non-empty); others show count only
          const cards = p.devCards.length
            ? `<span style="margin-left:8px;color:#c6a;font-size:0.8em">[${p.devCards.join(', ')}]</span>`
            : p.devCardCount > 0
              ? `<span style="margin-left:8px;color:#c6a;font-size:0.8em">[${p.devCardCount} dev card(s)]</span>`
              : '';
          return `<div style="display:flex;align-items:center;gap:10px;padding:4px 0;border-bottom:1px solid rgba(255,255,255,0.05)">
            <div style="width:9px;height:9px;border-radius:50%;background:${col};flex-shrink:0"></div>
            <span style="color:${col};font-weight:bold;min-width:80px;font-size:0.85em">${escHtml(p.name)}</span>
            <span style="color:#f5d78e;min-width:40px;font-size:0.85em">${p.vp} VP</span>
            <span style="display:flex;gap:8px;font-family:monospace;font-size:0.82em;color:#e2e8f0">${res}</span>
            ${cards}
          </div>`;
        }).join('');
        snapHtml = `<div style="margin-top:8px;background:rgba(0,0,0,0.25);border-radius:6px;padding:8px 12px">${rows}</div>`;
      }

      html += `<div style="text-align:center;color:#4a7aab;letter-spacing:2px;font-size:0.82em;
        margin:24px 0 6px;border-top:1px solid #1e3a5c;padding-top:16px">${escHtml(entry.publicMessage)}</div>${snapHtml}`;
      html += `<div style="margin-bottom:10px"></div>`;
      continue;
    }

    // Commentary entries get a special callout style
    if (entry.phase === 'commentary') {
      const nameCol = playerHexMap[entry.fromName] ?? '#9ab8d8';
      html += `<div style="background:rgba(183,121,31,0.10);border:1px solid rgba(183,121,31,0.35);
        border-left:4px solid #b7791f;border-radius:6px;padding:8px 14px;margin-bottom:7px;margin-left:12px">
        <div style="display:flex;align-items:center;gap:8px;margin-bottom:4px">
          <span style="color:${nameCol};font-weight:bold;font-size:0.85em">${escHtml(entry.fromName)}</span>
          <span style="background:#b7791f;color:#fff;padding:1px 6px;border-radius:4px;font-size:0.7em;letter-spacing:1px">💬 ASIDE</span>
        </div>
        <div style="color:#f6e05e;font-style:italic;font-size:0.86em;line-height:1.5">${escHtml(entry.publicMessage)}</div>
      </div>`;
      continue;
    }

    const phaseBg  = PHASE_COLORS[entry.phase] ?? '#4a5568';
    const badge    = PHASE_LABELS[entry.phase] ? `<span style="background:${phaseBg};color:#fff;padding:2px 7px;border-radius:4px;font-size:0.72em;letter-spacing:1px;white-space:nowrap">${PHASE_LABELS[entry.phase]}</span>` : '';
    const nameCol   = playerHexMap[entry.fromName] ?? '#9ab8d8';
    const borderCol = entry.fromId ? (playerHexMap[entry.fromName] ?? '#2d3748') : '#2d3748';

    const publicHtml = entry.publicMessage
      ? `<div style="color:#e2e8f0;line-height:1.55;font-size:0.88em">${escHtml(entry.publicMessage)}</div>`
      : '';
    const reasoningHtml = entry.reasoning
      ? `<div style="color:#7a9bb5;font-style:italic;font-size:0.8em;margin-top:5px;line-height:1.45;border-top:1px solid rgba(255,255,255,0.06);padding-top:5px"><em>💭 ${escHtml(entry.reasoning)}</em></div>`
      : '';
    html += `<div style="background:rgba(255,255,255,0.04);border:1px solid rgba(255,255,255,0.07);
      border-left:4px solid ${borderCol};border-radius:6px;padding:10px 14px;margin-bottom:7px">
      <div style="display:flex;align-items:center;gap:8px;margin-bottom:5px">
        <span style="color:${nameCol};font-weight:bold;font-size:0.9em">${escHtml(entry.fromName)}</span>
        ${badge}
      </div>
      ${publicHtml}${reasoningHtml}
    </div>`;
  }

  const scoreboard = [...scores].sort((a, b) => b.vp - a.vp).map(p => {
    const col = p.colorHex;
    return `<div style="display:flex;align-items:center;gap:8px;margin:3px 0"><div style="width:10px;height:10px;border-radius:50%;background:${col}"></div><b>${escHtml(p.name)}</b><span style="color:#f5d78e;margin-left:4px">${p.vp} VP</span></div>`;
  }).join('');

  return `<!DOCTYPE html>
<html lang="en"><head><meta charset="UTF-8"><title>AI Catan — Turn ${turnNum} Dialogue (Seed ${seed})</title>
<style>*{box-sizing:border-box;margin:0;padding:0}body{background:#0d2b4a;font-family:'Georgia',serif;color:#f0e6d3;display:flex;flex-direction:column;align-items:center;min-height:100vh}
header{padding:18px 0 4px;font-size:1.4em;letter-spacing:4px;color:#f5d78e;text-transform:uppercase}
#sub{font-size:0.75em;color:#9ab8d8;letter-spacing:2px;margin-bottom:16px}
#wrap{width:100%;max-width:740px;padding:0 18px}
#final{background:rgba(255,255,255,0.05);border:1px solid rgba(255,255,255,0.1);border-radius:8px;padding:12px 16px;margin:20px 0 32px}</style></head>
<body><header>AI CATAN</header>
<div id="sub">TURN ${turnNum} DIALOGUE — SEED: ${seed}</div>
<div id="wrap">
${html}
<div id="final"><div style="font-size:0.78em;letter-spacing:2px;color:#9ab8d8;margin-bottom:8px">SCORES AFTER TURN ${turnNum}</div>${scoreboard}</div>
</div></body></html>`;
}

function escHtml(s: string): string {
  return s.replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;').replace(/"/g,'&quot;');
}
